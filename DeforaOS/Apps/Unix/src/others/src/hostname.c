#include <unistd.h>
#include <stdio.h>


/* hostname */
static int _hostname(void)
{
	char buf[128];

	if(gethostname(buf, sizeof(buf)) != 0)
		return 1;
	printf("%s\n", buf);
	return 0;
}


/* usage */
static int _usage(void)
{
	fputs("Usage: hostname\n", stderr);
	return 1;
}


/* main */
int main(int argc, char * argv[])
{
	int o;

	while((o = getopt(argc, argv, "")) != -1)
		switch(o)
		{
			default:
				return _usage();
		}
	if(optind != argc)
		return _usage();
	return _hostname() == 0 ? 0 : 2;
}
